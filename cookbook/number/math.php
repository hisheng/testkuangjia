//对数 a的x 次方 为 y，那么x是以a为底，y的对数
echo log(8,2); //3
echo log(10,1.25);//10.318851158516 如果年收益 25%,那么翻10倍，需要10.3年

echo log(10,1.3);//8.7762908476408 年收益 30%，翻10倍，需要8.7年

echo log(10,1.5);//5.6788735872676 年收益 50% 翻10倍需要 5.7年


// 也就是 计算指数，计算幂 ， 就是 a的x次方是多少

var_dump(pow(2,3));//int 8
var_dump(pow(1.25,10.32));//float 10.002563894308
var_dump(pow(1.3,8.77)); //float 9.9835086770139